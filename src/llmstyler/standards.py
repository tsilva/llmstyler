DEFAULT_ARTIFACT_VERSION = "v1"


def artifact_version(config: dict[str, Any]) -> str:
    value = config.get("version") or config.get("artifact_version") or DEFAULT_ARTIFACT_VERSION
    version = str(value).strip()
    if not version:
        version = DEFAULT_ARTIFACT_VERSION
    if re.fullmatch(r"\d+(?:\.\d+)*", version):
        version = f"v{version}"
    return slugify(version)


def slugify(value: Any) -> str:
    slug = str(value).strip().lower()
    slug = re.sub(r"[^a-z0-9._-]+", "-", slug)
    slug = re.sub(r"-{2,}", "-", slug)
    slug = re.sub(r"\.{2,}", ".", slug)
    slug = slug.strip(".-")
    if not slug:
        raise ValueError("artifact name cannot be empty after slug normalization")
    return slug


def versioned_slug(value: Any, version: str) -> str:
    slug = slugify(value)
    suffix = slugify(version)
    return slug if slug.endswith(f"-{suffix}") else f"{slug}-{suffix}"


def standard_repo_id(
    hub: dict[str, Any], *, fallback_name: str, version: str, required: bool = True
) -> str | None:
    repo_id = hub.get("repo_id")
    if repo_id:
        return str(repo_id)
    owner = hub.get("owner")
    if owner:
        return f"{owner}/{versioned_slug(fallback_name, version)}"
    if required:
        raise KeyError("hub.repo_id or hub.owner is required for publishing")
    return None


def now_iso() -> str:
    return datetime.now(UTC).isoformat()


def markdown_table(headers: list[str], rows: Iterable[Iterable[Any]]) -> str:
    rendered = ["| " + " | ".join(headers) + " |"]
    rendered.append("| " + " | ".join("---" for _ in headers) + " |")
    for row in rows:
        rendered.append("| " + " | ".join(_cell(value) for value in row) + " |")
    return "\n".join(rendered)


