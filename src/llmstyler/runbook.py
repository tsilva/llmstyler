def with_style_system(messages):
    messages = list(messages)
    if not STYLE_SYSTEM_PROMPT:
        return messages
    style_turn = {{"role": "system", "content": STYLE_SYSTEM_PROMPT}}
    if messages and messages[0].get("role") == "system":
        return [style_turn] + messages[1:]
    return [style_turn] + messages


def format_messages(batch):
    styled = [with_style_system(messages) for messages in batch["messages"]]
    texts = tokenizer.apply_chat_template(styled, tokenize=False, add_generation_prompt=False)
    if tokenizer.bos_token:
        texts = [text.removeprefix(tokenizer.bos_token) for text in texts]
    return {{"text": texts}}


