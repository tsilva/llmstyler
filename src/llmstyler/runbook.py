def metrics_markdown(train_metrics=None, eval_metrics=None):
    sections = []
    if train_metrics:
        sections.append("### Train")
        sections.append("")
        sections.append("| Metric | Value |")
        sections.append("| --- | --- |")
        for key, value in sorted(train_metrics.items()):
            sections.append("| " + str(key) + " | " + str(value) + " |")
        sections.append("")
    if eval_metrics:
        sections.append("### Evaluation")
        sections.append("")
        sections.append("| Metric | Value |")
        sections.append("| --- | --- |")
        for key, value in sorted(eval_metrics.items()):
            sections.append("| " + str(key) + " | " + str(value) + " |")
        sections.append("")
    if not sections:
        return "Metrics were not available when this card was written."
    return "\\n".join(sections).strip()


def write_model_card(repo_id, artifact_kind, path, metrics):
    if not repo_id:
        return None
    card = (
        MODEL_CARD_TEMPLATE.replace("__REPO_ID__", repo_id)
        .replace("__ARTIFACT_KIND__", artifact_kind)
        .replace(
            "__METRICS_SECTION__",
            metrics_markdown(metrics.get("train"), metrics.get("eval")),
        )
    )
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(card + "\\n", encoding="utf-8")
    return path


